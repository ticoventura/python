#1) Faça um programa que leia dois números e calcule:
a = float(input("Digite um número: "))
b = float(input("Digite outro número: "))

#a) A soma.
soma = a+b
print(f"A soma dos números é: {soma}")

#b) A subtração.
sub = a-b
print(f"A subtração dos números é: {sub}")

#c) A multiplicação.
mult = a*b
print(f"A multiplicação dos números é: {mult}")

#d) A divisão.
div = a/b
print(f"A divisão dos números é: {div}")

#e) A exponenciação.
exp = a**b
print(f"A exponenciação de {a} elevado a {b} é: {exp}")

#f) O módulo.
mod = a%b
print(f"{a} módulo {b} é: {mod}")

#g) O resto.
resto = a%b
print(f"O resto da divisão de {a} por {b} é: {resto}")